    {
        Unit::create([
            'name' => $request->name,
            'abbreviature' => $request->abbreviature,
            'companieId' => Auth::user()->companieId,
        ]);

        session()->flash('success', 'unidad Creada.');
        
        return redirect('/units');
    }

